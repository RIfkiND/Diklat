  Tugas dan kewajiban guru adalah mengelola pembelajaran dengan baik mulai dari perencanaan, pelaksanaan,
  termasuk
  melakukan evaluasi agar terorganisasikan dengan baik. Pengelolaan pembelajaran ini mendorong proses
  pembelajaran
  terlaksana dengan baik sehingga menciptakan pendidikan berkualitas dalam pencapaian tujuan pembelajaran.
  Standar
  Kompetensi Guru mencakup 4 (empat) kompetensi, yaitu kompetensi pedagogik, kepribadian, sosial, dan
  profesional yang
  dibuktikan dengan sertifikat pendidik. Kompetensi keahlian di bidang kejuruan yang merupakan kompetensi
  profesional,
  dibuktikan dengan sertifikat keahlian yang relevan sesuai ketentuan peraturan perundang-undangan. <br>

  Sebagai unit pelaksana teknis Kementerian Pendidikan, Kebudayaan, Riset, dan Teknologi yang bertugas untuk
  melakukan
  pengembangan dan penjaminan mutu PTK Vokasi, Balai Besar Pengembangan Penjaminan Mutu Pendidikan Vokasi
  Bidang Mesin dan
  Teknik Industri (BBPPMPV BMTI) telah melaksanakan program upskilling dan reskilling bagi PTK Vokasi
  khususnya guru
  kejuruan. <br>

  Untuk mengetahui sejauh mana pelaksanaan RTL yang telah disusun oleh peserta di akhir pelatihan dan apakah
  tujuan
  pelatihan telah tercapai dan dapat memberikan manfaat yang signifikan bagi peserta diklat, unit kerja, serta
  siswanya
  maka BBPPMPV BMTI memandang penting untuk melakukan Pendampingan Pelaksanaan RTL dan Evaluasi Dampak
  Pelatihan terhadap
  peserta yang telah mengikuti Peningkatan Kualitas dan Kompetensi PTK Vokasi melalui Pelatihan dan Program
  Upskilling dan
  Reskilling tahun 2024.
